CREATE TABLE CLIENTES (
    ID_CLIENTE NUMBER(3) PRIMARY KEY,
    NOMBRE VARCHAR2(50),
    CORREO VARCHAR2(50),
    CONTRASEÑA VARCHAR2(50),
    TELEFONO NUMBER(9),
    PUNTOS NUMBER(5)
);

CREATE TABLE EMPLEADOS (
    ID_EMPLEADO NUMBER(3) PRIMARY KEY,
    NOMBRE VARCHAR2(50),
    APELLIDOS VARCHAR2(50),
    PUESTO VARCHAR(25),
    SALARIO NUMBER(4),
    TURNO VARCHAR2(10),
    FECHA_CONTRATACIÓN DATE
);

CREATE TABLE PRODUCTOS (
    ID_PRODUCTO NUMBER(3) PRIMARY KEY,
    NOMBRE VARCHAR2(50),
    CATEGORIA VARCHAR2(25),
    PRECIO NUMBER(4),
    STOCK NUMBER(4),
    ID_PROVEEDOR NUMBER(3),
    CONSTRAINT FK_PROVEEDOR FOREIGN KEY ID_PROVEEDOR REFERENCES TO PROVEEDORES (ID_PROVEEDOR)
);

CREATE TABLE PROVEEDORES (
    ID_PROVEEDOR NUMBER(3) PRIMARY KEY,
    NOMBRE VARCHAR2(50),
    TELEFONO NUMBER(9),
    EMAIL VARCHAR2(50),
    DIRECCION VARCHAR2(50)
);

CREATE TABLE PEDIDOS (
    ID_PEDIDO NUMBER(4) PRIMARY KEY,
    FECHA DATE,
    TOTAL NUMBER(5,2),
    ID_EMPLEADO NUMBER(3),
    ID_CLIENTE NUMBER(3),
    CONSTRAINT FK_EMPLEADO FOREIGN KEY ID_EMPLEADO REFERENCES TO EMPLEADOS (ID_EMPLEADO),
    CONSTRAINT FK_CLIENTE FOREIGN KEY ID_CLIENTE REFERENCES TO CLIENTES (ID_CLIENTE)
);

--Tabla de detalles específicos de cada pedido
CREATE TABLE DETALLE_PEDIDO (
    ID_DETALLE NUMBER(3) PRIMARY KEY,
    ID_PEDIDO NUMBER(4),
    ID_PRODUCTO NUMBER(3),
    CANTIDAD NUMBER(2),
    PRECIO_UNITARIO NUMBER(4,2),
    SUBTOTAL NUMBER(4,2),
    CONSTRAINT FK_PEDIDO FOREIGN KEY ID_PEDIDO REFERENCES TO PEDIDOS (ID_PEDIDO),
    CONSTRAINT FK_PRODUCTO FOREIGN KEY ID_PRODUCTO REFERENCES TO PRODUCTOS (ID_PRODUCTO)
);

CREATE TABLE INVENTARIO (
    ID_MOVIMIENTO NUMBER(3) PRIMARY KEY,
    ID_PRODUCTO NUMBER(3),
    CANTIDAD NUMBER(3),
    FECHA DATE,
    CONSTRAINT FK_PRODUCTO2 FOREIGN KEY ID_PRODUCTO REFERENCES TO PRODUCTOS (ID_PRODUCTO)
);